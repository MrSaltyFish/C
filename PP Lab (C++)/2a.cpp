// numbers given by the user in C++

#include <iostream>
using namespace std;

int main()
{
  int num1, num2;
  cout << "Enter two numbers: ";
  cin >> num1 >> num2;

  int sum = num1 + num2;
  cout << "The sum of the two numbers is: " << sum << endl;

  return 0;
}
